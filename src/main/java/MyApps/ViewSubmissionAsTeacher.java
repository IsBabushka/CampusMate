    private void saveButtonMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_saveButtonMouseReleased
        // TODO add your handling code here:
        String gradeStr = gradeTextField.getText().trim();
        if (gradeStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a grade.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            int grade = Integer.parseInt(gradeStr);
            if (grade < 0 || grade > 100) {
                JOptionPane.showMessageDialog(this, "Grade must be between 0 and 100.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            TaskSubmissionDAO taskSubmissionDAO = new TaskSubmissionDAO();
            taskSubmissionDAO.update(submission.getSubmissionId(), submission.getSubmissionTitle(), submission.getSubmissionContent(), grade, TaskStatus.SUBMITTED, submission.getStudentId(), submission.getTaskId());
            JOptionPane.showMessageDialog(this, "Grade updated successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Invalid grade format. Please enter a number.", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "An error occurred while updating the grade.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_saveButtonMouseReleased

