    private void populateFields() {
        try {
            if (submission != null) {
                TaskDAO taskDAO = new TaskDAO();
                Task task = taskDAO.findById(submission.getTaskId());

                if (task != null) {
                    taskTitleText.setText(task.getTaskTitle());
                    taskPromptArea.setText(task.getTaskPrompt());
                } else {
                    taskTitleText.setText("Task not found");
                    taskPromptArea.setText("");
                }

                submissionTitleText.setText(submission.getSubmissionTitle());
                submissionContentArea.setText(submission.getSubmissionContent());
                // Pre-fill grade if already graded
                if (submission.getSubmissionGrade() != null) {
                    gradeTextField.setText(String.valueOf(submission.getSubmissionGrade()));
                } else {
                    gradeTextField.setText("");
                }
            } else {
                // Handle case where submission is null
                JOptionPane.showMessageDialog(this, "Submission data is missing.", "Error", JOptionPane.ERROR_MESSAGE);
                this.dispose(); // Close the window if submission data is missing
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "An error occurred.", "Error", JOptionPane.ERROR_MESSAGE);
            this.dispose(); // Close the window if an error occurs
        }
    }



